<script lang="ts">

export default {
  props: {
    value: Boolean
  },
  setup(props: any, context: any) {
    const toggle = () => {
      context.emit('update:value', !props.value)
    }
    return {toggle}
  }
}
</script>

<template>
  <button class="gulu-switch" @click="toggle" :class="{'gulu-checked':value}">
    <span></span>
  </button>
</template>

<style lang="scss">
$h: 22px;
$h2: 18px;
.gulu-switch {
  height: $h;
  width: $h*2;
  border: none;
  background: #bfbfbf;
  border-radius: calc($h / 2);
  position: relative;

  > span {
    position: absolute;
    top: 2px;
    left: 2px;
    height: $h2;
    width: $h2;
    background: white;
    border-radius: calc($h / 2);
    transition: all 250ms;
  }

  &.gulu-checked {
    background: #50C878;

    > span {
      left: calc(100% - #{$h2} - 2px);
    }
  }

  &:focus {
    outline: none;
  }

  &:active {
    > span {
      width: $h2 + 4px;
    }
  }

  &.gulu-checked:active {
    > span {
      width: $h2 + 4px;
      margin-left: -4px;
    }
  }
}
</style>